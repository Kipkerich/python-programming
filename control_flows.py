# Control flow is the order in which your code is to be executed

#if statement
temperature = 30

if temperature > 25:
    print("It's a hot day")


#if else statement
temperature = 20

if temperature > 25:
    print ("It's a hot day!")
else:
    print("It's a cool day!")


#if.. elif..else statement
temperature = 15

if temperature > 25:
    print("It's a hot day!")
elif temperature > 15:
    print("it's a warm day!")
else:
    print("It's a cold day!")

